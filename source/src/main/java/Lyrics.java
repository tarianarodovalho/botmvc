
public class Lyrics {

	private Artist artist;
	private Song song;
	private String text;

	public Artist getArtist() {
		return artist;
	}

	public void setArtist(Artist artist) {
		this.artist = artist;
	}

	public Song getSong() {
		return song;
	}

	public void setSong(Song song) {
		this.song = song;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

}